def main() -> None:
    en_items = {i["id"]: i for i in json.loads(EN_PATH.read_text(encoding="utf-8"))}
    pt_items = json.loads(PT_PATH.read_text(encoding="utf-8"))
    # keep only ids present in PT (already cleaned)
    leftover = []
    for it in pt_items:
        src = en_items.get(it["id"], it)
        desc_en = src.get("descriptionEn") or it.get("descriptionEn") or ""
        it["descriptionPt"] = translate(desc_en)
        it["typeLinePt"] = translate_type(src.get("typeLineEn") or it.get("typeLineEn") or "")
        if EN_LEFTOVER.search(it["descriptionPt"]):
            leftover.append(it["id"])
        it["translationMethod"] = "full-glossary-v2"

    PT_PATH.write_text(json.dumps(pt_items, ensure_ascii=False, indent=2), encoding="utf-8")

    slim = []
    for i in pt_items:
        slim.append(
            {
                "id": i["id"],
                "name": i["namePt"] or i["nameEn"],
                "nameEn": i["nameEn"],
                "category": i["category"],
                "typeLine": i.get("typeLinePt") or "",
                "rarity": i["rarity"],
                "requiresAttunement": i["requiresAttunement"],
                "description": i["descriptionPt"],
                "source": "basic-rules",
                "variants": i.get("variants") or [],
            }
        )
    SLIM.write_text(json.dumps(slim, ensure_ascii=False, indent=2), encoding="utf-8")

    report = ROOT / "content/items/TRANSLATION_AUDIT.md"
    report.write_text(
        "\n".join(
            [
                "# Auditoria de tradução PT-BR",
                "",
                f"- Itens: {len(pt_items)}",
                f"- Descrições ainda com possíveis resíduos EN: **{len(leftover)}**",
                "",
                "## IDs com resíduo EN detectado",
                "",
                *[f"- `{x}`" for x in leftover[:100]],
                ("…" if len(leftover) > 100 else ""),
            ]
        ),
        encoding="utf-8",
    )
    print(f"translated {len(pt_items)}; leftover_flags={len(leftover)}")


if __name__ == "__main__":
    main()
