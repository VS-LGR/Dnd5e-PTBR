def feet_to_m(text: str) -> str:
    def repl(m: re.Match[str]) -> str:
        n = int(m.group(1))
        meters = round(n * 0.3, 1)
        if float(meters).is_integer():
            meters = int(meters)
        return f"{meters} metros"

    text = re.sub(r"(\d+)\s*[-–]?\s*foot(?:-radius)?", repl, text, flags=re.I)
    text = re.sub(r"(\d+)\s*feet", repl, text, flags=re.I)
    text = re.sub(r"(\d+)\s*ft\.?", repl, text, flags=re.I)
    return text


def translate(text: str) -> str:
    s = text
    # ligatures
    s = s.replace("ﬁ", "fi").replace("ﬂ", "fl").replace("ﬀ", "ff").replace("ﬃ", "ffi")
    for en, pt in sorted(PHRASES, key=lambda x: len(x[0]), reverse=True):
        s = s.replace(en, pt)
    s = feet_to_m(s)
    for pat, rep in WORD_MAP:
        s = re.sub(pat, rep, s)
    s = re.sub(r"\s{2,}", " ", s).strip()
    s = re.sub(r"\s+([.,;:])", r"\1", s)
    return s


